package com.example.ex17;

public class Printer {
	static void Printerln(int value) {
		System.out.println(value);
	}
	
	static void Printerln(boolean value) {
		System.out.println(value);
	}
	
	static void Printerln(double value) {
		System.out.println(value);
	}
	
	static void Printerln(String value) {
		System.out.println(value);
	}
}

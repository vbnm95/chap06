package com.example.ex16;

public class Printer {
	void Printerln(int value) {
		System.out.println(value);
	}
	
	void Printerln(boolean value) {
		System.out.println(value);
	}
	
	void Printerln(double value) {
		System.out.println(value);
	}
	
	void Printerln(String value) {
		System.out.println(value);
	}
}
